     <section id="historia">
        <h2>Historia</h2>
        <p>
            El Día de Muertos es una celebración que se remonta a las antiguas civilizaciones de Mesoamérica, en especial a la cultura azteca. Los aztecas creían que la muerte no era el final de la vida, sino una parte del ciclo natural. Para ellos, los difuntos podían regresar a la Tierra durante el Día de Muertos para reunirse con sus seres queridos.
        </p>
        <p>
            Las festividades se realizaban en el noveno mes del calendario azteca, que corresponde aproximadamente a finales de octubre y principios de noviembre. Con la llegada de los conquistadores españoles en el siglo XVI, esta tradición se fusionó con elementos del catolicismo, convirtiéndose en una celebración que honra a los muertos, pero que también refleja la cultura y las creencias indígenas.
        </p>
        <p>
            En 2008, la UNESCO reconoció el Día de Muertos como Patrimonio Cultural Inmaterial de la Humanidad, destacando su importancia no solo en México, sino en muchas comunidades latinas alrededor del mundo.
        </p>
    </section>
    <section id="tradiciones">
        <h2>Tradiciones</h2>
        <p>
            Las tradiciones del Día de Muertos son ricas y variadas, reflejando la diversidad cultural de México. Entre las más destacadas se encuentran:
        </p>
        <ul>
            <li><strong>Ofrendas:</strong> Altares que se preparan en casa y en los cementerios, adornados con fotografías, objetos personales, alimentos y bebidas que le gustaban al difunto.</li>
            <li><strong>Calaveritas de azúcar:</strong> Dulces en forma de calavera decorados con glaseado de colores que representan la vida y la muerte.</li>
            <li><strong>Visitas a los cementerios:</strong> Familias se reúnen para limpiar y decorar las tumbas de sus seres queridos, llevando flores, velas y comida.</li>
            <li><strong>Desfiles y festivales:</strong> En muchas ciudades, se celebran desfiles y festivales con música, danzas y vestimenta tradicional, mostrando la alegría de recordar a los que han partido.</li>
        </ul>
    </section>
    <section id="ofrendas">
        <h2>Elementos de la Ofrenda</h2>
        <p>
            Una ofrenda típica incluye diversos elementos que simbolizan la conexión entre los vivos y los muertos. Estos elementos son:
        </p>
        <ul>
            <li><strong>Fotografías de los difuntos:</strong> Colocadas en el altar para recordar a aquellos que han partido.</li>
            <li><strong>Flores de cempasúchil:</strong> Estas flores, de color amarillo y naranja, son un símbolo de la muerte y se cree que guían a las almas con su fragancia.</li>
            <li><strong>Pan de muerto:</strong> Un pan dulce con forma de calavera o huesos, que se ofrece a los muertos y se comparte entre los vivos.</li>
            <li><strong>Incienso y velas:</strong> Utilizados para purificar el espacio y guiar a las almas hacia el altar.</li>
            <li><strong>Bebidas y alimentos:</strong> Comidas y bebidas que eran favoritas del difunto, para que puedan disfrutarlas en su regreso.</li>
        </ul>
    </section>
    <section id="recetas">
        <h2>Recetas Típicas</h2>
        <p>
            La gastronomía juega un papel fundamental en la celebración del Día de Muertos. Algunas recetas tradicionales incluyen:
        </p>
        <ul>
            <li><strong>Pan de muerto:</strong> Un pan dulce que se elabora especialmente para esta fecha, con un sabor a naranja y espolvoreado con azúcar.</li>
            <li><strong>Tamales:</strong> Hechos de masa de maíz rellena de carne, verduras o frijoles, envueltos en hojas de maíz y cocidos al vapor.</li>
            <li><strong>Atole:</strong> Una bebida caliente a base de masa de maíz, agua o leche, endulzada y aromatizada con sabores como vainilla o chocolate.</li>
            <li><strong>Calaveritas de azúcar:</strong> Dulces que son elaborados con azúcar y decorados con glaseado, que son parte esencial de las ofrendas.</li>
        </ul>
    </section>
    <section id="cultura">
        <h2>Importancia Cultural</h2>
        <p>
            El Día de Muertos es una celebración que va más allá del luto. Es un momento de recordar, celebrar y honrar a los seres queridos que han fallecido. Para muchas comunidades, es un tiempo de reflexión sobre la vida y la muerte, y un espacio para compartir recuerdos y anécdotas.
        </p>
        <p>
            Las festividades fomentan la unión familiar y comunitaria, ya que amigos y familiares se reúnen para recordar a los que han partido. Además, promueven la transmisión de tradiciones y la identidad cultural de las comunidades. El Día de Muertos también es un recordatorio de que la muerte es parte de la vida, y que los recuerdos de quienes han fallecido viven en el corazón de los vivos.
        </p>
